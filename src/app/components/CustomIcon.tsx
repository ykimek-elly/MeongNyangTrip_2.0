import React from 'react';

interface IconProps {
  size?: number;
  className?: string;
}

/**
 * Filled 아이콘 팩토리 — fill 기반 (Heroicons Solid 등)
 * paths: string 또는 string[] 지원
 */
export function createFilledIcon(displayName: string, paths: string | string[], viewBox = '0 0 24 24') {
  const pathArr = Array.isArray(paths) ? paths : [paths];
  const Icon = ({ size = 24, className }: IconProps) => (
    <svg
      xmlns="http://www.w3.org/2000/svg"
      width={size}
      height={size}
      viewBox={viewBox}
      fill="currentColor"
      role="presentation"
      className={className}
    >
      {pathArr.map((d, i) => (
        <path key={i} d={d} fillRule="evenodd" clipRule="evenodd" />
      ))}
    </svg>
  );
  Icon.displayName = displayName;
  return Icon;
}

/**
 * Outline 아이콘 팩토리 — stroke 기반 (Heroicons Outline 등)
 * paths: string 또는 string[] 지원
 */
export function createOutlineIcon(displayName: string, paths: string | string[], strokeWidth = 1.5, viewBox = '0 0 24 24') {
  const pathArr = Array.isArray(paths) ? paths : [paths];
  const Icon = ({ size = 24, className }: IconProps) => (
    <svg
      xmlns="http://www.w3.org/2000/svg"
      width={size}
      height={size}
      viewBox={viewBox}
      fill="none"
      stroke="currentColor"
      strokeWidth={strokeWidth}
      strokeLinecap="round"
      strokeLinejoin="round"
      role="presentation"
      className={className}
    >
      {pathArr.map((d, i) => <path key={i} d={d} />)}
    </svg>
  );
  Icon.displayName = displayName;
  return Icon;
}

